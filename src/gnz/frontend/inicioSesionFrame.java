    private void inicioSesionjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inicioSesionjButtonActionPerformed
        String user = this.userjTextField.getText();
        String password = this.jPasswordField1.getText();
        if (user.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe completar los campos");
        } else {
            try {
                if (ManejadorDeUsuarios.buscarUsuario(user, password)) {
                    this.dispose();
                    new FrameOM(user);
                }else{
                    JOptionPane.showMessageDialog(this, "El usuario o contrasena son incorrectas, pruebe nuevamente");
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_inicioSesionjButtonActionPerformed

